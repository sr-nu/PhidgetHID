    /**
     * initalize the phidget device and link the event listeners
     **/
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
        //Tweak up the UI to insert our custom drawing panel for the motion graph.
        //This will initialize the motion graph panel and get this aligned correctly.
        this.setupUI();
        
        this.axis1SensitivityScrl.setEnabled(false);
        this.axis1SensitivityScrl.setValueIsAdjusting(true);
        this.axis1SensitivityScrl.setMinimum(0);
        this.axis1SensitivityScrl.setMaximum(100);
        this.axis1SensitivityScrl.setValue(0);
        this.axis1SensitivityScrl.setValueIsAdjusting(false);
        this.jLabel13.setText("(0.0)");
        
        this.axis2SensitivityScrl.setEnabled(false);
        this.axis2SensitivityScrl.setValueIsAdjusting(true);
        this.axis2SensitivityScrl.setMinimum(0);
        this.axis2SensitivityScrl.setMaximum(100);
        this.axis2SensitivityScrl.setValue(0);
        this.axis2SensitivityScrl.setValueIsAdjusting(false);
        this.jLabel14.setText("(0.0)");
        
        this.axis3SensitivityScrl.setEnabled(false);
        this.axis3SensitivityScrl.setValueIsAdjusting(true);
        this.axis3SensitivityScrl.setMinimum(0);
        this.axis3SensitivityScrl.setMaximum(100);
        this.axis3SensitivityScrl.setValue(0);
        this.axis3SensitivityScrl.setValueIsAdjusting(false);
        this.jLabel15.setText("(0.0)");
        
        try
        {
            accel = new AccelerometerPhidget();
            
            attach_listener = new AccelAttachListener(this, this.graphPanel, this.attachedTxt,
                    this.nameTxt, this.serialTxt, this.versionTxt, this.numAxesTxt,
                    this.rangeTxt, this.axis1SensitivityScrl, this.axis2SensitivityScrl,
                    this.axis3SensitivityScrl, this.jLabel13, this.jLabel14, this.jLabel15);
            
            detach_listener = new AccelDetachListener(this, this.graphPanel, this.attachedTxt, this.nameTxt,
                    this.serialTxt, this.versionTxt, this.numAxesTxt, this.rangeTxt,
                    this.accel1Txt, this.accel2Txt, this.accel3Txt, this.axis1SensitivityScrl,
                    this.axis2SensitivityScrl, this.axis3SensitivityScrl, this.jLabel13,
                    this.jLabel14, this.jLabel15);
            
            error_listener = new AccelErrorListener(this);
            
            change_listener = new AccelChangeListener(this, this.graphPanel, this.accel1Txt, this.accel2Txt, this.accel3Txt);
            
            accel.addAttachListener(attach_listener);
            accel.addDetachListener(detach_listener);
            accel.addErrorListener(error_listener);

            accel.addAccelerationChangeListener(change_listener);
            accel.openAny();
        }
        catch(PhidgetException ex)
        {
            JOptionPane.showMessageDialog(this, ex.getDescription(), "Phidget Error" + ex.getErrorNumber(), JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formWindowOpened

