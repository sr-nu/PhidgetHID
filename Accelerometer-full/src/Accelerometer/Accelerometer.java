/**
 * When the application is being terminated, close the Phidget
 **/
private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        try
        {
            //unhook the event listeners
            accel.removeAccelerationChangeListener(change_listener);
            accel.removeErrorListener(error_listener);
            accel.removeDetachListener(detach_listener);
            accel.removeAttachListener(attach_listener);
            
            //close the Accelerometer phidget
            accel.close();
            accel = null;
            
            dispose();
            System.exit(0);
        }
        catch(PhidgetException ex)
        {
            JOptionPane.showMessageDialog(this, ex.getDescription(), "Phidget Error" + ex.getErrorNumber(), JOptionPane.ERROR_MESSAGE);
            dispose();
            System.exit(0);
        }
}//GEN-LAST:event_formWindowClosed
    
