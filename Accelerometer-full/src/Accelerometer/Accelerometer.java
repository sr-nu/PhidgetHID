private void axis1SensitivityScrlStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_axis1SensitivityScrlStateChanged
    try
    {
        if((this.axis1SensitivityScrl.isEnabled()) && ((this.accel.isAttached()) || (this.accel.isAttachedToServer())))
        {

                double value = (double)this.axis1SensitivityScrl.getValue() / 100;
                this.accel.setAccelerationChangeTrigger(0, value);
                this.jLabel13.setText("(" + value + ")");

        }
    }
    catch(PhidgetException ex)
    {
        JOptionPane.showMessageDialog(this, ex.getDescription(), "Phidget Error" + ex.getErrorNumber(), JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_axis1SensitivityScrlStateChanged

